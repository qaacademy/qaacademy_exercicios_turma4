package exemplosMetodosSemRetorno;

public class ExemploSemRetorno {

    public static void main(String[] args) {
        verificarMatricula();
        verificarFaltas();
        verificarAprovacao();
    }

    public static void verificarAprovacao() {
        System.out.println("Aprovado");
    }

    public static void verificarFaltas() {
        System.out.println("2 faltas");

    }

    public static void verificarMatricula() {
        System.out.println("Matriculado");

    }

}
