package intanciaDeclasse;

public class ExerciciosParametros {

    public  void exibePalavraParametro(String palavra) {
        System.out.println(palavra);
    }

    public  void exibeNumeParametro(int numero) {
        System.out.println(numero);
    }

    public  void exibeDoubleParametro(double numero) {
        System.out.println(numero);
    }

    public  void exibeBooleanParametro(boolean estado) {
        System.out.println(estado);
    }

    public  void exibeFloatParametro(float numero) {
        System.out.println(numero);
    }

    public  String exibePalavraParametroComRetorno(String palavra) {
        return palavra;
    }

    public  int exibeNumeroParametroComRetorno(int numero) {
        return numero;
    }

    public  double exibeDoubleParametroRetorno(double numero) {
        return numero;
    }

    public  boolean exibeBooleanParametroRetorno(boolean estado) {
        return estado;
    }

    public  float exibeFloatParametroRetorno(float numero) {
        return numero;
    }

}
